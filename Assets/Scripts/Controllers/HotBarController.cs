using System.Collections.Generic;
using UnityEngine;
using UnityEngine.InputSystem;
using static UnityEditor.Progress;

public class HotBarController : MonoBehaviour
{
    public GameObject hotBarPanel;
    public GameObject slotPrefab;
    public int slotCount = 5;

    Vector3 originalScale;

    private ItemDictionary itemDictionary;

    private Key[] hotbarKeys;


    private void Awake()
    {
        itemDictionary = FindObjectOfType<ItemDictionary>();

        hotbarKeys = new Key[slotCount];
        for(int i = 0; i < slotCount; i++)
        {
            hotbarKeys[i] = i < 6 ? (Key)((int)Key.Digit1 + i) : Key.Digit0;
        }
    }

    // Update is called once per frame
    void Update()
    {
        for (int i = 0; i < slotCount; i++)
        {
            if (Keyboard.current[hotbarKeys[i]].wasPressedThisFrame)
            {
                //UseItem
                UseItemInSlot(i);
            }
        }
    }


    void UseItemInSlot(int index)
    {
        Slot slot = hotBarPanel.transform.GetChild(index).GetComponent<Slot>();
        if (slot.currentItem != null) {
            Item item = slot.currentItem.GetComponent<Item>();
            item.UseItem();
        }
    }


    public List<InventorySaveData> GetHotbarItems()
    {
        List<InventorySaveData> invData = new List<InventorySaveData>();

        foreach (Transform slotTransform in hotBarPanel.transform)
        {
            Slot slot = slotTransform.GetComponent<Slot>();

            if (slot.currentItem != null)
            {
                Item item = slot.currentItem.GetComponent<Item>();
                invData.Add(new InventorySaveData { itemID = item.ID, slotIndex = slotTransform.GetSiblingIndex() });
            }
        }

        return invData;
    }


    public void SetHotbarItems(List<InventorySaveData> inventorySaveData)
    {
        //Create inventory panel
        foreach (Transform child in hotBarPanel.transform)
        {
            Destroy(child.gameObject);
        }

        //Create new Slots
        for (int i = 0; i < slotCount; i++)
        {
            Instantiate(slotPrefab, hotBarPanel.transform);
        }


        //Populate slots with saved items
        foreach (InventorySaveData data in inventorySaveData)
        {

            if (data.slotIndex < slotCount)
            {
                Slot slot = hotBarPanel.transform.GetChild(data.slotIndex).GetComponent<Slot>();

                GameObject itemPrefab = itemDictionary.GetItemPrefab(data.itemID);

                
                if (itemPrefab != null)
                {
                    GameObject item = Instantiate(itemPrefab, slot.transform);
                    item.GetComponent<RectTransform>().anchoredPosition = Vector2.zero;
                    slot.currentItem = item;

                    Item itemComponent = item.GetComponent<Item>();

                    if (itemComponent != null) {
                        if (slot.CompareTag("SmallSlot"))
                            item.transform.localScale = itemComponent.transform.localScale * 0.7f;
                        else
                            item.transform.localScale = itemComponent.transform.localScale;
                    }
                }
            }
        }


    }
}
